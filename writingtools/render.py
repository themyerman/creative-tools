def _starter_card(key: str, card: dict, genre: dict, voice: str | None, influence: str | None) -> str:
    icon = genre.get("icon", "✦")
    label = genre.get("label", key.title())
    color = genre.get("color", "#1a1a1a")

    voice_badge = (
        f'<span class="voice-badge">{voice}</span>'
        if voice else ""
    )
    lens_row = (
        f'<div class="card-lens">via {influence}</div>'
        if influence else ""
    )

    prompt_text = card.get("prompt", "")
    opening_text = card.get("opening", "")

    opening_html = (
        f'<div class="card-opening">&ldquo;{opening_text}&rdquo;</div>'
        if opening_text else ""
    )

    return f"""
  <div class="card">
    <div class="card-header" style="background: {color};">
      <span>{icon}</span>
      <span>{label}</span>
      {voice_badge}
    </div>
    {lens_row}
    <div class="card-body">
      <div class="card-prompt">{prompt_text}</div>
      {opening_html}
    </div>
  </div>"""


def render_email(
    cards: dict[str, dict],
    genres: dict,
    voice_map: dict | None = None,
    influence_map: dict | None = None,
) -> str:
    today = date.today().strftime("%A, %B %-d, %Y")

    cards_html = "\n".join(
        _starter_card(
            key,
            card,
            genres.get(key, {}),
            (voice_map or {}).get(key),
            (influence_map or {}).get(key),
        )
        for key, card in cards.items()
    )

    return f"""<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <title>Daily Writing Spark — {today}</title>
  <style>{_CSS}</style>
</head>
<body>
  <div class="wrapper">
    <div class="header">
      <h1>✦ Daily Writing Spark</h1>
      <div class="date">{today}</div>
    </div>
    {cards_html}
    <div class="footer">
      Generated by creative-tools &middot; GitHub Models &middot; {today}
    </div>
  </div>
</body>
</html>"""
